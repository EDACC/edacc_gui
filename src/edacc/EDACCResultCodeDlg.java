    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        try {
            int code = Integer.parseInt(tfResultCode.getText());
            String description = tfDescription.getText();
            ManageDBResultCodes.getInstance().saveNewCode(code, description);
            dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                    "Error while saving ResultCode to DB: " + ex.getMessage(),
                    "Error while saving", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                    "Error while saving ResultCode to DB: ResultCode must be a "
                    + "number!",
                    "Error while saving", JOptionPane.ERROR_MESSAGE);
            tfResultCode.selectAll();
            tfResultCode.requestFocusInWindow();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Error while saving ResultCode: " + ex.getMessage(),
                    "Error while saving", JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_bSaveActionPerformed

