    @Override
    public boolean include(Object value) {
        if (!chkActivate.isSelected()) {
            return true;
        }
        if (value instanceof String) {
            String val = (String) value;
            try {
                return val.matches(txtRegexp.getText());
            } catch (Exception e) {
                return false;
            }
        }
        return true;
    }

