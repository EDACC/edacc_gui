    @Override
    public boolean include(Object value) {
        int comp;
        if (value instanceof Integer) {
            Integer val = (Integer) value;
            comp = val.compareTo(valInt);
        } else if (value instanceof Double) {
            Double val = (Double) value;
            comp = val.compareTo(valDouble);
        } else if (value instanceof Float) {
            Float val = (Float) value;
            comp = val.compareTo(valFloat);
        } else {
            return false;
        }
        if ("<".equals(compStr)) {
            return comp < 0;
        } else if ("<=".equals(compStr)) {
            return comp <= 0;
        } else if ("==".equals(compStr)) {
            return comp == 0;
        } else if (">=".equals(compStr)) {
            return comp >= 0;
        } else if (">".equals(compStr)) {
            return comp > 0;
        }
        return false;
    }

    public static boolean accept(Class<?> clazz) {
        return clazz == Integer.class || clazz == Double.class || clazz == Float.class;
    }
