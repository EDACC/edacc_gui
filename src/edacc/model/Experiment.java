package edacc.model;

import java.sql.Date;

public class Experiment extends BaseModel implements IntegerPKModel {

    private int id;
    private String description;
    private Date date;
    private int maxSeed;
    private int numRuns;
    private int CPUTimeLimit;
    private int memoryLimit;
    private int wallClockTimeLimit;
    private int stackSizeLimit;
    private int outputSizeLimit;
    private boolean autoGeneratedSeeds;
    private boolean linkSeeds;
    private String name;

    @Override
    public int hashCode() {
        return 31 + id;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Experiment other = (Experiment) obj;
        if (this.id != other.id) {
            return false;
        }
        return true;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public boolean isAutoGeneratedSeeds() {
        return autoGeneratedSeeds;
    }

    public void setAutoGeneratedSeeds(boolean autoGeneratedSeeds) {
        this.autoGeneratedSeeds = autoGeneratedSeeds;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public boolean isLinkSeeds() {
        return linkSeeds;
    }

    public void setLinkSeeds(boolean linkSeeds) {
        this.linkSeeds = linkSeeds;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    @Override
    public int getId() {
        return id;
    }

    protected void setId(int id) {
        this.id = id;
    }

    public int getMaxSeed() {
        return maxSeed;
    }

    public void setMaxSeed(int maxSeed) {
        this.maxSeed = maxSeed;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public int getNumRuns() {
        return numRuns;
    }

    public void setNumRuns(int numRuns) {
        this.numRuns = numRuns;
    }

    public int getCPUTimeLimit() {
        return CPUTimeLimit;
    }

    public void setCPUTimeLimit(int CPUTimeLimit) {
        this.CPUTimeLimit = CPUTimeLimit;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public int getMemoryLimit() {
        return memoryLimit;
    }

    public void setMemoryLimit(int memoryLimit) {
        this.memoryLimit = memoryLimit;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public int getOutputSizeLimit() {
        return outputSizeLimit;
    }

    public void setOutputSizeLimit(int outputSizeLimit) {
        this.outputSizeLimit = outputSizeLimit;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public int getStackSizeLimit() {
        return stackSizeLimit;
    }

    public void setStackSizeLimit(int stackSizeLimit) {
        this.stackSizeLimit = stackSizeLimit;
        if (this.isSaved()) {
            this.setModified();
        }
    }

    public int getWallClockTimeLimit() {
        return wallClockTimeLimit;
    }

    public void setWallClockTimeLimit(int wallClockTimeLimit) {
        this.wallClockTimeLimit = wallClockTimeLimit;
        if (this.isSaved()) {
            this.setModified();
        }
    }
}
