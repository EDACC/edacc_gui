    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        instanceGeneratorUnifKCNFController controller = new instanceGeneratorUnifKCNFController();
        String message = "";
        boolean itemsMissing = false;
        int k, n, step, stop, num;
        double r;
        boolean series, genClass;
        if (this.tfK.getText().isEmpty()) {
            message += "\"Maximum clause length\" \n";
            itemsMissing = true;
        }
        if (this.tfR.getText().isEmpty()) {
            message += "\"Ratio\" \n";
            itemsMissing = true;
        }
        if (this.tfVStart.getText().isEmpty()) {
            message += "\"Number of variables\" \n";
            itemsMissing = true;
        }
        if (itemsMissing) {
            message = "Please provide values for the following fields:\n" + message;
        }
        if ((this.cbSeries.isSelected()) && (!this.tfVStep.getText().isEmpty() && !this.tfVStart.getText().isEmpty() && this.tfVStop.getText().isEmpty())) {
            message += "You have specified a start value for the number of variables and a step!\n";
            message += "Please provide also the \"stop at\" value!";
            itemsMissing = true;
        }

        if (itemsMissing) {
            JOptionPane.showMessageDialog(this.getParent(),
                    message,
                    "Missing specifications!",
                    JOptionPane.WARNING_MESSAGE);
        } else if ((this.cbSeries.isSelected()) && (Integer.parseInt(this.tfVStop.getText()) < Integer.parseInt(this.tfVStart.getText()))) {
            JOptionPane.showMessageDialog(this.getParent(),
                    "Start value for the number of variables is larger than stop value",
                    "Number of variables range error",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            //hier m√ºssen alle parse-fehler noch abgefangen werden!!!!
            try {
                k = Integer.parseInt(this.tfK.getText());
                n = Integer.parseInt(this.tfVStart.getText());
                num = Integer.parseInt(this.tfNV.getText());
                //r = Double.parseDouble(this.tfR.getText());
                try {
                    r = DecimalFormat.getNumberInstance(Locale.ENGLISH).parse(tfR.getText()).doubleValue();
                    series = this.cbSeries.isSelected();
                    if (series) {
                        step = Integer.parseInt(this.tfVStep.getText());
                        stop = Integer.parseInt(this.tfVStop.getText());
                    } else {
                        step = n;
                        stop = 1;
                    }
                    genClass = this.cbGC.isSelected();
                    //das muss noch in einer progressbar rein!!!
                    //call Controller to generate and add instances
                    try {
                        controller.generate(k, r, n, series, step, stop, num, genClass, this.parentClass);
                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(this,
                                "There is a Problem with the database: " + ex.getMessage(),
                                "Error",
                                JOptionPane.ERROR_MESSAGE);
                    } catch (InstanceClassAlreadyInDBException ex) {
                        JOptionPane.showMessageDialog(this,
                                "Instance class is already in the system.",
                                "Error",
                                JOptionPane.ERROR_MESSAGE);
                    }

                    this.setVisible(false);
                } catch (ParseException ex) {
                    Logger.getLogger(EDACCInstanceGeneratorUnifKCNF.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this.getParent(),
                            "Values specified for ratio is invalid!",
                            "Invalid values specified!",
                            JOptionPane.ERROR_MESSAGE);
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this.getParent(),
                        "Values specified are invalid!" + ex.getMessage(),
                        "Invalid values specified!",
                        JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnGenerateActionPerformed

    private String getNumberText(String text) {
        String res = "";
        int begin;
        for (begin = 0; begin < text.length() && text.charAt(begin) == '0'; begin++);
        if (begin > 0 && begin == text.length()) {
            return "";
        }
        for (int i = begin; i < text.length(); i++) {
            if (text.charAt(i) >= '0' && text.charAt(i) <= '9') {
                res += text.charAt(i);
            }
        }
        return res;
    }
