    private void comboParameterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboParameterActionPerformed
        if (comboParameter.getSelectedItem() == null) {
            return;
        }
        String param = (String) comboParameter.getSelectedItem();
        SpecifyDomainPanel domainPanel = domainPanels.get(param);
        if (domainPanel == null) {
            domainPanel = new SpecifyDomainPanel();
            domainPanels.put(param, domainPanel);
        }
        panel.removeAll();
        panel.add(domainPanel, BorderLayout.CENTER);
        panel.invalidate();
        panel.revalidate();
        panel.repaint();
    }//GEN-LAST:event_comboParameterActionPerformed

