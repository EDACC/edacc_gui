    public SpecifyDomainDialog(java.awt.Frame parent, boolean modal, ArrayList<String> parameters, Set<Parameter> savedParams) {
        super(parent, modal);
        initComponents();
        cancelled = true;
        panel.setLayout(new BorderLayout());
        domainPanels = new HashMap<String, SpecifyDomainPanel>();

        for (Parameter param : savedParams) {
            SpecifyDomainPanel domainPanel = new SpecifyDomainPanel();
            domainPanels.put(param.getName(), domainPanel);
            domainPanel.setDomain(param.getDomain());
        }

        comboParameter.removeAllItems();
        for (String s : parameters) {
            comboParameter.addItem(s);
        }

    }

