    public Set<Parameter> getParameters() {
        Set<Parameter> res = new HashSet<Parameter>();
        for (String paramName : domainPanels.keySet()) {
            SpecifyDomainPanel domainPanel = domainPanels.get(paramName);
            try {
                res.add(new Parameter(paramName, domainPanel.getDomain()));
            } catch (InvalidDomainException ex) {
                // TODO: Error?
            }
        }
        return res;
    }
